import mongoose from "mongoose";
const coachSchema = mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
});
const CoachModel = mongoose.model("CoachModel", coachSchema);
export default CoachModel;
